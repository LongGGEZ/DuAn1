    void load() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        try {
            List<HoaDon> list = dao.select();
            for (HoaDon hoaDon : list) {
                Object[] row = {
                    hoaDon.getMahoadon(),
                    hoaDon.getTongsp(),
                    hoaDon.getTongtien(),
                    hoaDon.getManv()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void load2() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        try {
            String keyword =  txtTimKiem.getText();
            List<HoaDon> list = dao.selectByKeyword(keyword);
            for (HoaDon hoaDon : list) {
                Object[] row = {
                    hoaDon.getMahoadon(),
                    hoaDon.getTongsp(),
                    hoaDon.getTongtien(),
                    hoaDon.getManv()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

